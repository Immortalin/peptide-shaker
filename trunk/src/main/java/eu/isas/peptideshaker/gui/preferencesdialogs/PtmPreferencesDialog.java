            peptideShakerGUI.getUserPreferences().setDeltaScoreThreshold(new Double(deltaThresholdTxt.getText()));
            peptideShakerGUI.getUserPreferences().setaScoreThreshold(new Double(aScoreThresholdTxt.getText()));
